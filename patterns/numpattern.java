public class numpattern {
    public static void main(String[] args) {
      int n=4, m=3;
      int num = 1;
        for(int i =1; i<=m; i++){
          for(int j=1; j<=n; j++){
            System.out.print(num+" ");
            num++;
          }
          
          System.out.println();
        }
    
}
}
